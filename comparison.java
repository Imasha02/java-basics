// Comparison operators If-Else
public class comparison {
    public static void main(String args[]) {
        int x = 100;

        if (x == 100) {
            System.out.println("equal to 100");
        }
        if (x != 50) {
            System.out.println("Not equal to 50");
        }
        if (x > 10) {
            System.out.println("Greater than 10");
        }
        if (x < 200) {
            System.out.println("Lesser than 200");
        }
        if  (x >= 100) {
            System.out.println("Greater than or equal to 100");
        }
        /*else
        {
            System.out.println("Wrong");
        }*/
    }
}

